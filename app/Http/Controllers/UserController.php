     */
    public function store(Request $request)
    {
        $this->validate($request,[
            'name' => 'required|min:3',
            'email' => 'required|min:7',
            'password' => 'required|min:6',
            'phone' => 'required|min:10'
        ]);

        $add = new User();
        $add->fill($request->all());
        $add->save();
